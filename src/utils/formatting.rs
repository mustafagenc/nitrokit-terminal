use chrono::{DateTime, Utc};
use colored::*;

pub fn format_date(timestamp: i64) -> String {
    let dt = DateTime::<Utc>::from_timestamp(timestamp, 0);
    match dt {
        Some(date) => date.format("%Y-%m-%d %H:%M:%S UTC").to_string(),
        None => "Invalid date".to_string(),
    }
}

